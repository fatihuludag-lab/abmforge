from __future__ import annotations

from typing import Any, Protocol


class StorageBackend(Protocol):
    """Protocol for ABMForge dataset storage backends."""

    def add_run(self, **metadata: Any) -> None: ...

    def update_last_run(self, **metadata: Any) -> None: ...

    def record_model(self, **record: Any) -> None: ...

    def record_agent(self, **record: Any) -> None: ...

    def record_event(self, **record: Any) -> None: ...

    def record_lifecycle(self, **record: Any) -> None: ...

    def record_error(self, **record: Any) -> None: ...
