from __future__ import annotations

from dataclasses import dataclass

from abmforge.experiment.seed_sequence import SeedSequence


@dataclass(frozen=True, slots=True)
class ReplicatePlanEntry:
    """Planned replicate run for one parameter-grid position."""

    parameter_index: int
    replicate_index: int
    run_index: int
    seed: int

    def to_dict(self) -> dict[str, int]:
        """Return a stable dictionary representation."""
        return {
            "parameter_index": self.parameter_index,
            "replicate_index": self.replicate_index,
            "run_index": self.run_index,
            "seed": self.seed,
        }


def build_replicate_plan(
    *,
    parameter_count: int,
    replicates: int,
    seed_sequence: SeedSequence,
    label: str | None = None,
    start_run_index: int = 0,
) -> list[ReplicatePlanEntry]:
    """Build a deterministic replicate plan.

    The plan is ordered in parameter-major order:

    ``parameter_index=0, replicate_index=0..N``,
    then ``parameter_index=1, replicate_index=0..N``, and so on.

    Seeds are generated by ``SeedSequence`` in the same linear run order.
    This keeps the helper deterministic while leaving actual experiment
    execution unchanged.
    """
    parameter_count = _validate_non_negative_int("parameter_count", parameter_count)
    replicates = _validate_non_negative_int("replicates", replicates)
    start_run_index = _validate_non_negative_int("start_run_index", start_run_index)

    if not isinstance(seed_sequence, SeedSequence):
        raise TypeError("seed_sequence must be a SeedSequence")

    total_runs = parameter_count * replicates
    seeds = seed_sequence.generate(
        total_runs,
        start=start_run_index,
        label=label,
    )

    plan: list[ReplicatePlanEntry] = []
    local_run_index = 0

    for parameter_index in range(parameter_count):
        for replicate_index in range(replicates):
            run_index = start_run_index + local_run_index
            plan.append(
                ReplicatePlanEntry(
                    parameter_index=parameter_index,
                    replicate_index=replicate_index,
                    run_index=run_index,
                    seed=seeds[local_run_index],
                )
            )
            local_run_index += 1

    return plan


def _validate_non_negative_int(name: str, value: int) -> int:
    if isinstance(value, bool) or not isinstance(value, int):
        raise TypeError(f"{name} must be an integer")

    if value < 0:
        raise ValueError(f"{name} must be >= 0")

    return value
